    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        String username = this.txtUser.getText();
        String password = new String(this.jPassField.getPassword());

        try {
            String token = KeycloakAuth.getToken(username, password);
            this.lblResultado.setText("Bienvenido");
            System.out.println(token);
            User.token = token;
            Usuario usuario = usuarioService.obtenerUsuarioDesdeToken(token);
            String rol = usuario.getRol();
            objVtnPrincipal = new VtnPrincipalAdmin();
            
            objVtnPrincipal.gestionRol(rol);
            objVtnPrincipal.setVisible(true); // Abre la ventana principal
            this.txtUser.setText(null);
            this.jPassField.setText(null);
        } catch (Exception ex) {
            this.lblResultado.setText("Credenciales invalidas");
            this.txtUser.setText(null);
            this.jPassField.setText(null);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

